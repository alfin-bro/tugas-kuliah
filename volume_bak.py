# MENGHITUNG VOLUME BAK

# MENDEKLARASIKAN VARIABLE
panjang = 5
lebar = 6
tinggi = 3

# MENAMPILKAN PANJANG, LEBAR DAN TINGGI
print(f"Panjang: {panjang}")
print(f"Lebar: {lebar}")
print(f"Tinggi: {tinggi}")

# MENDEKLARASIKAN RUMUS VOLUME BAK
# VOLUME = PxLxT
volume = panjang*lebar*tinggi
print(f"Volume bak adalah = {volume}")
